##Task 1: Perform Basic Mathematical Operations Using Input and print functions

# Get Input from User For 2 numbers and convert the input to integer using typecast
num1= int(input("Enter the first number: "))
num2= int(input("Enter the second number: "))

#Person mathematical operation on the numbers recived
addition= num1 + num2
difference= num1 - num2
multiply= num1 * num2
division= num1 / num2

#Print the result of each operation back to use on console
print("Addition is: ", addition)
print("Difference is: ", difference)
print("Multiplication is: ", multiply)
print("Division is: ", division)
